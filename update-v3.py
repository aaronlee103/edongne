        articles = fetch_articles(rc)
        by_cat = {}
        for art in articles:
            c = art['category']
            if c not in by_cat:
                by_cat[c] = []
            by_cat[c].append(art)

        templates = make_templates(rd)
        for cat, art_list in by_cat.items():
            tpls = templates.get(cat, [])
            for idx, art in enumerate(sorted(art_list, key=lambda x: x['id'])):
                if idx < len(tpls):
                    content = tpls[idx]
                    clen = len(content)
                    ok = update_article(art['id'], content)
                    status = "✓" if ok else "✗"
                    short = " ⚠SHORT" if clen < 2000 else ""
                    print(f"  {status} {cat} #{idx+1} [{clen:>4}자]{short} {art['title'][:45]}")
                    if ok:
                        total_ok += 1
                    if clen < 2000:
                        total_short += 1

